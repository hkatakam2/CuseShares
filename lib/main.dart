
import 'package:firebase_core/firebase_core.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

import 'firebase_options.dart'; // Generated by flutterfire configure
import 'models/app_user.dart';
import 'services/auth_service.dart';
import 'services/firestore_service.dart';
import 'services/storage_service.dart';
import 'services/notification_service.dart';
import 'repositories/auth_repository.dart';
import 'repositories/post_repository.dart';
import 'viewmodels/auth_viewmodel.dart';
import 'viewmodels/home_viewmodel.dart';
import 'views/auth/login_screen.dart';
import 'views/home/home_screen.dart';

void main() async {
  // Ensure Flutter bindings are initialized
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  
  // Initialize Notification Service (run initialization)
  final notificationService = NotificationService();
  await notificationService.initialize();


  // Create instances of services
  final authService = AuthService();
  final firestoreService = FirestoreService();
  final storageService = StorageService();

  // Create instances of repositories, injecting services
  final authRepository = AuthRepository(authService: authService);
  final postRepository = PostRepository(
    firestoreService: firestoreService,
    storageService: storageService,
    authService: authService, // PostRepository needs authService too
  );


  runApp(
    // Use MultiProvider to provide multiple objects down the widget tree
    MultiProvider(
      providers: [
        // Provide Services (if needed directly by UI, usually not in pure MVVM)
        // Provider<AuthService>.value(value: authService),
         Provider<NotificationService>.value(value: notificationService),

        // Provide Repositories (can be useful for creating ViewModels on the fly)
        Provider<AuthRepository>.value(value: authRepository),
        Provider<PostRepository>.value(value: postRepository),

        // Provide ViewModels (ChangeNotifierProvider for state management)
        ChangeNotifierProvider<AuthViewModel>(
          create: (_) => AuthViewModel(authRepository: authRepository),
        ),
         // HomeViewModel can be provided here if it's relatively simple
         // and doesn't need arguments specific to the HomeScreen build context.
         // Otherwise, provide it closer to where it's used (e.g., in AuthWrapper).
         ChangeNotifierProvider<HomeViewModel>(
           create: (_) => HomeViewModel(postRepository: postRepository),
         ),
        // Other ViewModels (like CreatePostViewModel, PostDetailsViewModel)
        // are often created closer to their respective screens using
        // ChangeNotifierProvider or Consumer, especially if they need
        // specific data passed during creation (like a FoodPost object).
      ],
      child: MyApp(),
    ),
  );
}

