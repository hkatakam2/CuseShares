// Widget to handle authentication state and navigate accordingly
class AuthWrapper extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    // Listen to the authentication status from AuthViewModel
    final authViewModel = Provider.of<AuthViewModel>(context);

    print("AuthWrapper build triggered. Status: ${authViewModel.status}"); // Debug log

    switch (authViewModel.status) {
      case AuthStatus.authenticated:
        print("Navigating to HomeScreen");
        return HomeScreen(); // User is logged in, show Home
      case AuthStatus.unauthenticated:
      case AuthStatus.error: // Show login also on error state
         print("Navigating to LoginScreen");
        return LoginScreen(); // User is not logged in, show Login
      case AuthStatus.authenticating:
      case AuthStatus.uninitialized:
      default:
         print("Showing Loading Screen");
        // Show a loading indicator while checking auth state or authenticating
        return Scaffold(
          body: Center(
            child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                    CircularProgressIndicator(),
                    SizedBox(height: 10),
                    Text("Loading...")
                ],
            ),
          ),
        );
    }
  }
}
